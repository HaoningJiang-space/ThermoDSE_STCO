    def __str__(self):
        header = ['order', 'latency', 'noc (pJ)', 'nop (pJ)', 'dram (pJ)', 'comp (pJ)']
        for buf_name in self.buf_cost_dict.keys():
            header += [buf_name + ' (pJ)']
        header += ['energy (pJ)', 'btlnect']
        str_table = [header]
        for order in range(len(self.order_latency)):
            info = [order]
            energy_tot = 0
            latency = self.order_latency[order]
            noc_cost = self.order_noc_cost[order]
            nop_cost = self.order_nop_cost[order]
            dram_cost = self.order_dram_cost[order]
            comp_cost = self.order_comp_cost[order]
            info.append(latency)
            info.append(noc_cost)
            info.append(nop_cost)
            info.append(dram_cost)
            info.append(comp_cost)
            energy_tot += nop_cost + nop_cost + dram_cost + comp_cost
            for energy_list in self.buf_cost_dict.values():
                energy_tmp = energy_list[order]
                energy_tot += energy_tmp
                info.append(energy_tmp)
            info.append(energy_tot)
            if self.bottleneck[order] == 0:
                info.append('in')
            elif self.bottleneck[order] == 1:
                info.append('comp')
            elif self.bottleneck[order] == 2:
                info.append('out')
            else:
                raise ValueError(f'In execution order {order}, the bottleneck should be (nop move in)/(compute)/(nop '
                                 f'move out)')
            str_table.append(info)
        info = ['total']
        latency_tot = sum(self.order_latency)
        energy_tot = 0
        info.append(latency_tot)
        noc_cost = sum(self.order_noc_cost)
        nop_cost = sum(self.order_nop_cost)
        dram_cost = sum(self.order_dram_cost)
        comp_cost = sum(self.order_comp_cost)
        energy_tot += nop_cost + dram_cost + comp_cost
        info += [noc_cost, nop_cost, dram_cost, comp_cost]
        for energy_list in self.buf_cost_dict.values():
            energy_tot += sum(energy_list)
            info += [sum(energy_list)]
        info.append(energy_tot)
        info.append('-')
        str_table.append(info)
        str_ = tabulate(str_table, headers='firstrow', tablefmt='grid')
        return str_
